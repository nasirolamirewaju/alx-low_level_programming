#include <stdio.h>
/**
 * add - add two integers
 * @a: first int
 * @b: second int
 * Return: addition
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - sub two integers
 * @a: first int
 * @b: second int
 * Return: subtraction
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - mul two integers
 * @a: first int
 * @b: second int
 * Return: multiplication
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - div two integers
 * @a: first int
 * @b: second int
 * Return: division
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - mod two integers
 * @a: first int
 * @b: second int
 * Return: modulus
 */
int mod(int a, int b)
{
	return (a % b);
}
